// Create a map to store the file structure
const path = require('path');
const fs = require('fs-extra');

let fileMap = new Map();
let rootNodes = new Set();

// Function to add files and groups
function add(fileId, groupId, displayName, filePath) {
    let file = { text: displayName };
    if (filePath) {
        file.link = filePath;
    }
    file.collapsed = true

    // Store the file in the map
    fileMap.set(fileId, file);

    // If it's the root group
    if (fileId === groupId) {
        rootNodes.add(fileId);
    } else {
        // Find the parent group
        let parent = fileMap.get(groupId);
        if (!parent.items) {
            parent.items = [];
        }
        parent.items.push(file);
    }
}

// Function to output the JSON
function output() {
    let result = [];
    // Iterate over the map and build the result
    rootNodes.forEach((rootId) => {
        result.push(fileMap.get(rootId));
    });
    // console.log(JSON.stringify(result, null, 2));
    const output = `module.exports = ${JSON.stringify(result, null, 2)};`;
    fs.writeFileSync(path.join(__dirname, 'docs/.vitepress/sidebar.js'), output);
}

